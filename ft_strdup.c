#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		c;
	char	*dst;

	c = 0;
	
	while (src[c])
	{
		dst[c] = src[c];
		c++;
	}
	dst[c] = '\0';
	return (dst);
}
