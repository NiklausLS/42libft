#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	c;
	unsigned int	len;

	c = 0;
	if (!s || !f)
		return (0);
	len = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (0);
	while (c < len)
	{
		str[c] = f(c, s[c]);
		c++;
	}
	str[c] = '\0';
	return (str);
}
